package com.infosyst.bean;

public class User {
	private int id,edad,ingresosmens;
	private String name,ap,am,email,fechaing;
	
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getAp() {
		return ap;
	}
	public void setAp(String ap) {
		this.ap = ap;
	}
	
	public String getAm() {
		return am;
	}
	public void setAm(String am) {
		this.am = am;
	}
	public String getEmail() {
		return email;
	}
	public void setEmail(String email) {
		this.email = email;
	}
	public String getFechaIng() {
		return fechaing;
	}
	public void setFechaIng(String fechaing) {
		this.fechaing = fechaing;
	}
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public int getEdad() {
		return edad;
	}
	public void setEdad(int edad) {
		this.edad = edad;
	}
	public int getIngresosMens() {
		return ingresosmens;
	}
	public void setIngresomens(int ingresosmens) {
		this.ingresosmens = ingresosmens;
	}
	

}


